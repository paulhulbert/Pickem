    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if ( jRadioButton1.isSelected() || jRadioButton3.isSelected() ) {
            if ( jRadioButton2.isSelected() || jRadioButton4.isSelected() ) {
                if ( jTextField1.getText().equals("") || jTextField4.getText().equals("") || jTextField5.getText().equals("") ) {
                    return;
                } else {
                    
                    Object win;
                    Object lose;
                    int favored;
                    int hometeam;
                    
                    String score1;
                    String score2;
                    
//                    score1 = jTextField4.getText();
//                    score2 = jTextField5.getText();
                    
                    
                    
                    
                    if ( jRadioButton1.isSelected() ) {
                        hometeam = 2;
                    } else {
                        hometeam = 1;
                    }
                    
                    if ( jRadioButton2.isSelected() ) {
                        win = jComboBox1.getSelectedItem();
                        lose = jComboBox2.getSelectedItem();
                        score1 = jTextField4.getText();
                        score2 = jTextField5.getText();
                        favored = 1;
                        if ( hometeam == 1 ) {
                            hometeam = 2;
                        } else {
                            hometeam = 1;
                        }
                    } else {
                        win = jComboBox2.getSelectedItem();
                        lose = jComboBox1.getSelectedItem();
                        score2 = jTextField4.getText();
                        score1 = jTextField5.getText();
                        favored = 2;
                        
                    }
                    
                    
                    league1.addgame(win, lose, hometeam, favored, score1, score2, jTextField1.getText());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        if ( jRadioButton5.isSelected() || jRadioButton6.isSelected() ) {
            
                if ( jTextField2.getText().equals("") ) {
                    return;
                } else {
                    
                    Object win;
                    Object lose;
                    int hometeam;
                    
                    
                    int spread = Integer.parseInt(jTextField2.getText());
                    
                    
                    if ( jRadioButton5.isSelected() ) {
                        hometeam = 1;
                    } else {
                        hometeam = 2;
                    }
                    
                        win = jComboBox5.getSelectedItem();
                        lose = jComboBox6.getSelectedItem();
                        
                    
                        team fav = league1.stringtoteam(win);
                        team und = league1.stringtoteam(lose);
                    
                    guesser g = new guesser( fav, und, spread, hometeam, league1.ref );
                    
                    String tempstring;
                    
                    if ( g.answer > 40 ) {
                        tempstring = "<"+ g.answer;
                    } else {
                        tempstring = "" + g.answer + ">";
                    }
                    
                    jTextField6.setText(tempstring);
                    jTextField7.setText("" + g.score1);
                    jTextField8.setText("" + g.score2);
//                    jTextField6.setText("" + g.score1 + "-" + g.score2);
                }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

