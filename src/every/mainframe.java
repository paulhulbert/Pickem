    public void save()
    {
        try
        {
            saver = new ObjectOutputStream( new FileOutputStream( "data.ser" ));
            saver.writeObject(league1);
            
            if ( saver != null ) {
                saver.close();
            }
        }
        catch ( IOException ioException )
        {
            System.err.println( "Error Opening File" );
        }
        
        
    }
    
    public void load()
    {
        try
        {
            loader = new ObjectInputStream( new FileInputStream( "data.ser" ) );
            
            league1 = ( league ) loader.readObject();
            
            if ( loader != null ) {
                loader.close();
            }
            
        }
        catch ( IOException ioException )
        {
            System.err.println( "Error Opening File" );
        }
        catch ( ClassNotFoundException classNotFoundException ) {
            System.err.println( "Unable to create object" );
        }
    }

